#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Покупка билетов
===============
За билетами на премьеру нового мюзикла выстроилась очередь из
N человек, каждый из которых хочет купить 1 билет.
На всю очередь работала только одна касса, поэтому продажа билетов
шла очень медленно, приводя «постояльцев» очереди в отчаяние.

Самые сообразительные быстро заметили, что, как правило,
несколько билетов в одни руки кассир продаёт быстрее,
чем когда эти же билеты продаются по одному.
Поэтому они предложили нескольким подряд стоящим людям
отдавать деньги первому из них, чтобы он купил билеты на всех.

Однако для борьбы со спекулянтами кассир продавала не более 3
билетов в одни руки, поэтому договориться таким образом между
собой могли лишь 2 или 3 подряд стоящих человека.

Известно, что на продажу i-му человеку из очереди одного
билета кассир тратит Ai секунд, на продажу двух билетов —
Bi секунд, трёх билетов — Ci секунд.

Напишите программу, которая подсчитает минимальное время,
за которое могли быть обслужены все покупатели.

Обратите внимание, что билеты на группу объединившихся
людей всегда покупает первый из них.
Также никто в целях ускорения не покупает лишних
билетов (то есть билетов, которые никому не нужны).

Входные данные:
На вход программы поступает сначала число N —
количество покупателей в очереди (1 ≤N ≤5000).
Далее идут N троек натуральных чисел Ai, Bi, Ci.
Каждое из этих чисел не превышает 3600.
Люди в очереди нумеруются, начиная от кассы.

Выходные данные:
Требуется вывести одно число — минимальное время
в секундах, за которое могли быть обслужены все покупатели.

Пример:
  Ввод:
    5
    5 10 15
    2 10 15
    5 5 5
    20 20 1
    20 1 1
  Вывод:
    12
"""

n = int(input())
abc = []
for i in range(n):
    abc.append(tuple(map(int, input().split())))

t2 = t3 = 0
t1 = abc[0][0] # a[0]
if n > 1:
    t2, t1 = t1, min(t1 + abc[1][0], # t1 + a[1]
                     t2 + abc[0][1]) # t2 + b[0]
for i in range(2, n):
    t3, t2, t1 = \
        t2, t1, min(t1 + abc[i][0],   # t1 + a[i]
                    t2 + abc[i-1][1], # t2 + b[i-1]
                    t3 + abc[i-2][2]) # t3 + c[i-2]

print(t1)


